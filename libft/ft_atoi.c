#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		i;
	int		res;
	int		signe;
	char	*str;

	i = 0;
	res = 0;
	signe = 1;
	str = (char *)nptr;
	while ((str[i] >= 7 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = (-signe);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - 48);
		i++;
	}
	return (res * signe);
}
